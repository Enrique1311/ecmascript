// var
var lastName = "Enrique"; // Declaración y asignación de variable
lastName = "Spinelli"; // Reasignación de variable
console.log(lastName);

// let
let firstName = "Enrique"; // Declaración y asignación de variable
firstName = "Javier"; // Reasignación de variable
console.log(firstName);

// const
const name = "Enrique"; // Declaración y asignación de variable
// name = "Javier"; // No se puede reasignar una variable declarada con const
console.log(name);

let hello = "Hello";
let world = "World";
let phrase1 = hello + " " + world + "!";
console.log(phrase1);

// Template literals
let phrase2 = `${hello} ${world}!`;
console.log(phrase2);

// Multi-line strings
let lorem1 = "First line \n" + "second line";
console.log(lorem1);

let lorem2 = `First line
  second line`;
console.log(lorem2);
